			<h1>WHAT IS COLOR BLINDNESS</h1>
			<p>Color-blindness is the inability to distinguish the differences between certain colors. This condition results from an absence of color-sensitive pigment in the cone cells of the retina, the nerve layer at the back of the eye.</p>
			</div>
	</section>

