			<h2 class="heading" style="text-align:center;color:Red;">
				Red-green color blindness
			</h2>
      <p> This is the most common form, resulting in problems distinguishing between some shades of red, yellow, and green. There are four subtypes within this spectrum, each of which causes different color deficiencies.</p>
      <ul>
        <li>Protanomaly: Red, orange, and yellow appear greener and colors are not as bright.</li>
        <li>Protanopia: There are no working red cone cells, resulting in red appearing as black. Certain shades of orange, yellow, and green all look yellow.</li>
        <li>Deuteranopia: There are no working green cone cells, so reds look brownish-yellow and greens appear beige.</li>
        <li>Deuteranomaly is the most common type of red-green color blindness. It makes green look more red. This type is mild and doesn’t usually get in the way of normal activities.</li>
      </ul>
</div>

		<div class="d2">
			<img class="img-responsive" src="blueyellow.png" style="margin-left:450px;"  alt="">
			<h2 class="heading"style="text-align:center;color:blue;">
				Blue-yellow color blindness
			</h2>
			<p>This less-common type of color blindness makes it hard to tell the difference between blue and green, and between yellow and red.</p>
      <ul>
        <li>Tritanomaly makes it hard to tell the difference between blue and green, and between yellow and red.</li>
        <li>Tritanopia makes you unable to tell the difference between blue and green, purple and red, and yellow and pink. It also makes colors look less bright.</li>
		</div>
		<div class="d2">
			<img class="img-responsive" src="cc.png" style="margin-left:450px;" alt="">
			<h2 class="heading"style="text-align:center;color:violet;">
				Complete color blindness
			</h2>
			<p>If you have complete color blindness, you can’t see colors at all. This is also called monochromacy, and it’s quite uncommon. Depending on the type, you may also have trouble seeing clearly and you may be more sensitive to light.</p>
		</div>

